<template>
  <div>
<!-- ============================================================================================== -->
<!-- ========================================== Dialog ============================================ -->
<!-- ============================================================================================== -->
    <v-dialog persistent v-model="VueComponent">
      <v-card>
<!-- ============================================================================================== -->
<!-- ======================================== Card Title ========================================== -->
<!-- ============================================================================================== -->
        <v-card-title> 
          Component Dialog
          <v-spacer></v-spacer>
          <v-btn @click="ClickCloseDialog()" small>
            <v-icon>mdi-close</v-icon>
          </v-btn>
        </v-card-title>
<!-- ============================================================================================== -->
<!-- ========================================= Card Text ========================================== -->
<!-- ============================================================================================== -->
        <v-card-text>
          <v-container fluid>
            <div> <v-text-field label="String" v-model="strString"></v-text-field> </div>
            <div> <v-text-field label="Integer" v-model="intInteger"></v-text-field> </div>
            <div> <v-checkbox label="Boolean" v-model="blnBoolean"></v-checkbox> </div>
            <div>  OBJECT: {{ objObject }} </div>

            <div>
              ARRAY:
              <ul>
                <li v-for="(item,i) in arrArray" :key="i">
                  {{ item }}
                </li>
              </ul>
            </div>

          </v-container>
        </v-card-text>
<!-- ============================================================================================== -->
<!-- ======================================== Card Actions ======================================== -->
<!-- ============================================================================================== -->
        <v-card-actions>
          <v-btn @click="ClickConsoleButton()" block> Console </v-btn>
        </v-card-actions>
      </v-card>
    </v-dialog>
  </div>
</template>
<!-- ************************************************************************************************* -->
<!-- ============================================= Script ============================================ -->
<!-- ************************************************************************************************* -->
<script>
  export default {
// ********************************************************************************** //
// ====================================== Props ===================================== //
// ********************************************************************************** //
      props:{
        VueComponent:{
            type: Boolean,
            required: true,
        },
        VueComponentString:{
          type: String,
        },
        VueComponentInteger:{
          type: Number,
        },
        VueComponentBoolean:{
          type: Boolean,
        },
        VueComponentObject:{
          type: Object,
        },
        VueComponentArray:{
          type: Array,
        },
      },
// ********************************************************************************** //
// ====================================== Data ====================================== //
// ********************************************************************************** //
      data(){
        return{
          strString: undefined,
          intInteger: undefined,
          blnBoolean: false,
          objObject: {},
          arrArray: [],
        }
      },
// ********************************************************************************** //
// ====================================== Watch ===================================== //
// ********************************************************************************** //
      watch: {
        VueComponent(value){
          if(value === true) {
            this.strString = this.VueComponentString
            this.intInteger = this.VueComponentInteger
            this.blnBoolean = this.VueComponentBoolean
            this.objObject = this.VueComponentObject
            this.arrArray = this.VueComponentArray
          }
        }
      },
// ********************************************************************************** //
// ===================================== Methods ==================================== //
// ********************************************************************************** //
      methods:{
// ============================================================= //
// ===================== Data Load Events ====================== //
// ============================================================= //

// ============================================================= //
// ===================== User Action Events ==================== //
// ============================================================= //
        ClickConsoleButton(){
          this.$emit('EmitString', this.strString)
          this.$emit('EmitInteger', this.intInteger)
          this.$emit('EmitBoolean', this.blnBoolean)
          this.$emit('EmitObject', this.objObject)
          this.$emit('EmitArray', this.arrArray)
        },
        ClickCloseDialog(){
          this.$emit('ClickCloseDialog', false)
        },
      }
  }
</script>
<!-- ************************************************************************************************* -->
